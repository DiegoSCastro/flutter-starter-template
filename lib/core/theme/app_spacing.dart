/// Centralized spacing scale built on a 4px grid.
///
/// Use these tokens instead of hardcoded pixel values so padding, gaps, and
/// insets stay consistent across the app and can be tuned in one place.
abstract final class AppSpacing {
  /// 2px — hairline gaps.
  static const double xxs = 2;

  /// 4px.
  static const double xs = 4;

  /// 8px.
  static const double sm = 8;

  /// 12px.
  static const double md = 12;

  /// 16px — default content padding.
  static const double lg = 16;

  /// 20px.
  static const double xl = 20;

  /// 24px.
  static const double xxl = 24;

  /// 32px.
  static const double xxxl = 32;

  /// 40px.
  static const double xxxxl = 40;
}
